  function openArticleById(id, lang, opts = { push: false }) {
    const art = getArticleById(id, lang);
    if (!art) return;
  
    // Fill content (بدون صورة ولا meta)
    modalTitle.textContent = art.title;
    modalBody.innerHTML = art.longContent || `<p>${art.desc || ""}</p>`;
  
    // Show modal
    modal.classList.add("modal-open");
    document.body.classList.add("no-scroll");
    modal.setAttribute("aria-hidden", "false");
    setTimeout(() => modalClose.focus(), 0);
  
    // Update URL hash if requested
    if (opts.push) {
      const base = location.pathname + location.search;
      history.pushState(null, "", `${base}#article=${encodeURIComponent(id)}`);
      document.title =
        art.title + " — " + (translations[lang]["site-title"] || "");
    }
  }
  
  function closeArticle() {
    modal.classList.remove("modal-open");
    document.body.classList.remove("no-scroll");
    modal.setAttribute("aria-hidden", "true");
    if (location.hash.startsWith("#article=")) {
      history.replaceState(null, "", location.pathname + location.search);
      document.title = translations[getLang()]["site-title"] || "";
    }
  }
  
